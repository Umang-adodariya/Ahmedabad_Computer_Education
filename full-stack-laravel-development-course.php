                                            <li class="course-acc-li">Using query builder and Raw SQL to prepare the queries that can be further used for fetching information from the database, updating any existing records, validating the user inputs with the database details, and so on. </li>
                                            <li class="course-acc-li">Relationships are important in handling database queries by defining the connections and hierarchy of the tables including using the primary and foreign key pairs. </li>
                                            <li class="course-acc-li">Database transactions are necessary for performing a series of operations in the DB while behaving as a single unit. We will help you learn the ACID acronym necessary for developing and controlling these transactions, including Atomicity, Consistency, Isolation, and Durability.</li>
                                            </ul>
