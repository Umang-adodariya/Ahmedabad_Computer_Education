                                                <li><b>Game designer:</b> Your responsibility will be to design different mechanics involved with the game along with the rules, level designs, objectives, and the player’s experience. </li>
                                                <li><b>3D modeler:</b> From planning the layouts of the gaming software to developing the custom 3D elements to be included in the software, you will be responsible to design and develop a highly functional 3D model of the gaming software. </li>
                                                <li><b>UI/UX designer:</b> As a gaming UI/UX designer, you will be responsible for designing and developing an appealing and feature-rich interface so that the end users can have an immersive experience with the gaming software.</li>
